public class Roda {
    private int aro;
    private int calibragem;

    public Roda(int aro, int calibragem) {
        this.aro = aro;
        this.calibragem = calibragem;
    }

    public int getAro() {
        return aro;
    }

    public void setAro(int aro) {
        this.aro = aro;
    }

    public int getCalibragem() {
        return calibragem;
    }

    public void setCalibragem(int calibragem) {
        this.calibragem = calibragem;
    }
}
